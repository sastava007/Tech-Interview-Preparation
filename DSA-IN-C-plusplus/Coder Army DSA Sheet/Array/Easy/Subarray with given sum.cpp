    {
        // Your code here
        for(int i=0; i<n; i++){
            long long curr_sum = 0;
            for(int j = i; j<n; j++){
                curr_sum+=arr[j];
                if( curr_sum ==  s){
                    return {i+1, j+1};
                }
            }
        }
        
        return {-1};
    }
